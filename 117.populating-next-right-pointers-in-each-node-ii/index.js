/**
 * // Definition for a Node.
 * function Node(val,left,right,next) {
 *    this.val = val;
 *    this.left = left;
 *    this.right = right;
 *    this.next = next;
 * };
 */
/**
 * @param {Node} root
 * @return {Node}
 */
var connect = function(root) {
    if (!root) return root;
    var queue = [{
        node: root,
        level: 0
    }];

    while(queue.length) {
        var item = queue.shift();

        if (queue.length && queue[0].level === item.level) {
            // console.log(queue[0])
            // console.log('====')
            item.node.next = queue[0].node;
        }

        if (item.node.left) {
            queue.push({
                node: item.node.left,
                level: item.level + 1
            })
        }

        if (item.node.right) {
            queue.push({
                node: item.node.right,
                level: item.level + 1
            })
        }
    }

    return root;
};

